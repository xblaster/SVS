package net.lo2k;

import java.io.Serializable;

public class Person implements Serializable {
	/**
		 * 
		 */
	private static final long serialVersionUID = 5636217940179781990L;
	/**
		 * 
		 */
	private String name = "";
	private int age = 0;
	private String tel = "";
	private String adress = "";

	public Person() {
		super();
		// NOTHING
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	@Override
	public String toString() {
		return name + " " + age + " " + adress + " " + tel;
	}

	/**
	 * @param adress
	 *            the adress to set
	 */
	public void setAdress(String adress) {
		this.adress = adress;
	}

	/**
	 * @return the adress
	 */
	public String getAdress() {
		return adress;
	}
}